package models;

/**
 *
 * @author zsckare
 */
public class Marca {
    int idmarca;
    String marca, modelo;

    public int getIdmarca() {
        return idmarca;
    }

    public void setIdmarca(int idmarca) {
        this.idmarca = idmarca;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    @Override
    public String toString() {
        return "Marca{" + "idmarca=" + idmarca + ", marca=" + marca + ", modelo=" + modelo + '}';
    }
    
    
    
}
