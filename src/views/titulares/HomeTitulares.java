    void setData(){
        int filas = model.getRowCount();
        System.out.println(filas);
        if(filas> 0){
            System.out.print("Remover filas");
            for (int i = 0; i < model.getRowCount(); i++) {
                model.removeRow(1);
                
            }
            tableTitulares.setModel(model);
        }
        for (int i = 0; i < Comun.titulares.size(); i++) {
            Titular t = Comun.titulares.get(i);
            System.out.println(t.toString());
            model.addRow(new Object[]{t.getNombre(),t.getApellido(), t.getDireccion(), t.getTelefono(), t.getIdtipo(), t.getIdtaxi(), t.getClavesindical()});
            
        }
        tableTitulares.setModel(model);
    }
